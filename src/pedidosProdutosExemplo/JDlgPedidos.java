    public void habilitar(boolean valor) {
        Util.habilitar(valor, jTxtNumPedido, jFmtData, jCboCliente, jCboVendedor, jTxtTotal, jBtnConfirmar, jBtnCancelar);
        Util.habilitar(valor, jBtnIncluirProd, jBtnAlterarProd, jBtnExcluirProd);
        Util.habilitar(!valor, jBtnIncluir, jBtnAlterar, jBtnExcluir, jBtnPesquisar);
    }

    public Pedidos viewBean() {
        Pedidos pedidos = new Pedidos();
        pedidos.setIdpedidos(Integer.parseInt(jTxtNumPedido.getText()));
        pedidos.setData(Util.strDate(jFmtData.getText()));
        pedidos.setClientes((Clientes) jCboCliente.getSelectedItem());
        pedidos.setVendedor((Vendedor) jCboVendedor.getSelectedItem());
        pedidos.setTotal(Util.strDouble(jTxtTotal.getText()));
        return pedidos;
    }

    public void beanView(Pedidos pedidos) {
        jTxtNumPedido.setText(String.valueOf(pedidos.getIdpedidos()));
        jFmtData.setText(Util.dateStr(pedidos.getData()));
        jCboCliente.setSelectedItem(pedidos.getClientes());
        jCboVendedor.setSelectedItem(pedidos.getVendedor());
        jTxtTotal.setText(String.valueOf(pedidos.getTotal()));

        PedidosProdutosDAO pedidosProdutosDAO = new PedidosProdutosDAO();
        List listaProd = (List) pedidosProdutosDAO.listProdutos(pedidos);

        pedidosProdutosControle.setList(listaProd);
    }

    public int getSelectedRowProd() {
        return jTable1.getSelectedRow();
    }

