    private void jBtnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnIncluirActionPerformed
        Util.limparCampos(jTxtNumPedido, jFmtData, jCboCliente, jCboVendedor, jTxtTotal);
        habilitar(true);
        pedidosProdutosControle.setList(new ArrayList());
        jTxtNumPedido.grabFocus();
        incluindo = true;
        pedidos = new Pedidos();
    }//GEN-LAST:event_jBtnIncluirActionPerformed

    private void jBtnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAlterarActionPerformed
        if (pedidos != null) {
            habilitar(true);
            incluindo = false;
        } else {
            Util.mensagem("Deve ser realizada uma pesquisa antes");
        }
    }//GEN-LAST:event_jBtnAlterarActionPerformed

    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        if (pedidos != null) {
            if (Util.perguntar("Deseja excluir o pedido ?") == true) {
                PedidosProdutosDAO pedidosProdutosDAO = new PedidosProdutosDAO();
                PedidosProdutos pedidosProdutos;
                for (int linha = 0; linha < jTable1.getRowCount(); linha++) {
                    pedidosProdutos = pedidosProdutosControle.getBean(linha);
                    pedidosProdutosDAO.delete(pedidosProdutos);
                }
                pedidosDAO.delete(pedidos);
            }
        } else {
            Util.mensagem("Deve ser realizada uma pesquisa antes");
        }
        Util.limparCampos(jTxtNumPedido, jFmtData, jCboCliente, jCboVendedor, jTxtTotal);
        pedidos = null;
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        pedidos = viewBean();
        if (incluindo == true) {
            pedidosDAO.insert(pedidos);
            PedidosProdutosDAO pedidosProdutosDAO = new PedidosProdutosDAO();
            PedidosProdutos pedidosProdutos;
            for (int linha = 0; linha < jTable1.getRowCount(); linha++) {
                pedidosProdutos = pedidosProdutosControle.getBean(linha);
                pedidosProdutos.setPedidos(pedidos);
                pedidosProdutosDAO.insert(pedidosProdutos);
            }
        } else {
            pedidosDAO.update(pedidos);
            //remover todos os pedidos produtos deste pedido

            //incluir todos os pedidosProduto que estao no jtable
            PedidosProdutosDAO pedidosProdutosDAO = new PedidosProdutosDAO();
            PedidosProdutos pedidosProdutos;
            for (int linha = 0; linha < jTable1.getRowCount(); linha++) {
                pedidosProdutos = pedidosProdutosControle.getBean(linha);
                pedidosProdutos.setPedidos(pedidos);
                pedidosProdutosDAO.insert(pedidosProdutos);
            }
        }
        habilitar(false);
        Util.limparCampos(jTxtNumPedido, jFmtData, jCboCliente, jCboVendedor, jTxtTotal);
        pedidosProdutosControle.setList(new ArrayList());

        pedidos = null;
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

